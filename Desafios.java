package br.unipar.jogo;

public class Desafios {
    private String lixo;
    private String abastecer;
    private String meteoro;
    private String energia;
    private String alinhamento;
    private String cartao;
    private String baixar;

    public String getLixo() {
        return lixo;
    }

    public void setLixo(String lixo) {
        this.lixo = lixo;
    }

    public String getAbastecer() {
        return abastecer;
    }

    public void setAbastecer(String abastecer) {
        this.abastecer = abastecer;
    }

    public String getMeteoro() {
        return meteoro;
    }

    public void setMeteoro(String meteoro) {
        this.meteoro = meteoro;
    }

    public String getEnergia() {
        return energia;
    }

    public void setEnergia(String energia) {
        this.energia = energia;
    }

    public String getAlinhamento() {
        return alinhamento;
    }

    public void setAlinhamento(String alinhamento) {
        this.alinhamento = alinhamento;
    }

    public String getCartao() {
        return cartao;
    }

    public void setCartao(String cartao) {
        this.cartao = cartao;
    }

    public String getBaixar() {
        return baixar;
    }

    public void setBaixar(String baixar) {
        this.baixar = baixar;
    }

    
}
