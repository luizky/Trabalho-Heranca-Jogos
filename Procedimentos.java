package br.unipar.jogo;

public class Procedimentos {
    private String apertar;
    private String usar;
    private String reportar;
    public String getApertar() {
        return apertar;
    }

    public void setApertar(String apertar) {
        this.apertar = apertar;
    }

    public String getUsar() {
        return usar;
    }

    public void setUsar(String usar) {
        this.usar = usar;
    }

    public String getReportar() {
        return reportar;
    }

    public void setReportar(String reportar) {
        this.reportar = reportar;
    }



}
