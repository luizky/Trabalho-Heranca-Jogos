package br.unipar.jogo;

public class Movimento {
    private Void cima;
    private Void baixo;
    private Void esquerda;
    private Void direita;


    public Void getCima() {
        return cima;
    }

    public void setCima(Void cima) {
        this.cima = cima;
    }

    public Void getBaixo() {
        return baixo;
    }

    public void setBaixo(Void baixo) {
        this.baixo = baixo;
    }

    public Void getEsquerda() {
        return esquerda;
    }

    public void setEsquerda(Void esquerda) {
        this.esquerda = esquerda;
    }

    public Void getDireita() {
        return direita;
    }

    public void setDireita(Void direita) {
        this.direita = direita;
    }

}
